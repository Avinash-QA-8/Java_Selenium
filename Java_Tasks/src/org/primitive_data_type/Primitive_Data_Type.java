package org.primitive_data_type;

public class Primitive_Data_Type {
	
	                            // memory size
	
    static byte alpha;            // 1 byte (-128 to 127)
    
    static short beta;           // 2 byte ( -32768 to 32767 )
    
    static int charlie;             // 4byte 
    
    static long demo;            // 8 byte 
    
    static float eclipse;           // 4 byte
    
    static double forest;          // 8 byte
    
    static char goat;            // 2 byte
    
    static boolean happy;          // 1 bit
    
    public static void main (String [] args)
    
    {
    
    
    System.out.println(" Default Value: ");
    
    {
   	 System.out.println(alpha);
   	 
   	 System.out.println(beta);
   	 
   	 System.out.println(charlie);
   	 
   	 System.out.println(demo);
   	 
   	 System.out.println(eclipse);
   	 
   	 System.out.println(forest);
   	 
   	 System.out.println(goat);
   	 
   	 System.out.println(happy);
    }

}
    
}
